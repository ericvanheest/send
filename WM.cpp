DWORD GetWM(std::wstring strMessage)
{
    STRING2DWORD::iterator it;

    if (g_mapWM.empty())
        InitWM();

    it = g_mapWM.find(strMessage);
    if(it != g_mapWM.end() )
        return (*it).second;
    else
    {
        return (DWORD) ExpressionToDouble(strMessage);
    }
}